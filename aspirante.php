<div class="letrata"><center>Perfil de Aspirante</center><br></div>
<div class="letram" align="justify">
El aspirante a ingresar al programa de Ingeniería en 
Gestión Empresarial debe caracterizarse: <br><br><br>

<div class="letrat">Aptitudes:</div> <br>
1.-Razonamiento crítico-matemático.<br><br>                     
2.-Capacidad de creación e innovación.   <br><br>           
3.-Interés por las tendencias macroeconómicas y finanzas.<br><br> 
4.-Valorar la importancia de la capacitación y desarrollo del 
factor humano dentro de la organización o empresa.<br><br><br>  
<div class="letrat">Actitudes:</div><br>
1.- Gusto por las relaciones interpersonales.<br><br>              
2.- Disposición para trabajar en equipo. <br><br>                               
3.- Inclinación al orden y la limpieza.<br><br>      


